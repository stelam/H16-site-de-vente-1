    <h3>Le système permet :</h3>
    <h4>Général</h4>
    <ul>
        <li>de voir les spectacles présentement en vente;</li>
        <li>de configurer les spectacles présentés en priorité sur la page d'accueil du site de vente de billets.</li>
    </ul>
    <h4>Spectacles</h4>
    <ul>
        <li>de voir le nombre de billets disponibles et le nombre de billets vendus pour chaque spectacle;</li>
        <li>de créer, de modifier ou de supprimer un spectacle;</li>
        <li>de configurer le nom du spectacle, l'artiste, la date, la salle de spectacle, la description et le prix;</li>
        <li>de téléverser l'image et/ou la vignette d'un spectacle;</li>
        <li>de spécifier la période de disponibilité d'une vente de billets pour un spectacle;</li>
        <li>de créer des spectacles en mode invisible afin qu'ils ne soit pas affichés automatiquement sur le site à moins que l'administrateur coche une case à cocher permettant sa publication;</li>
        <li>de dupliquer un spectacle existant pour éviter de recopier toutes les informations pour la création d'une nouvelle représentation;</li>
    </ul>
    <h4>Salles de spectacle</h4>
    <ul>
        <li>de créer, de modifier ou de supprimer une salle de spectacle</li>
        <li>de configurer le nom de la salle, l'adresse et la capacité</li>
    </ul>
</div>